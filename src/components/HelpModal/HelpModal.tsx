import styles from "./HelpModal.module.css";

type Props = {
  open: boolean;
  onClose: () => void;
};

export default function HelpModal({ open, onClose }: Props) {
  if (!open) return null;

  return (
    <div className={styles.overlay} onClick={onClose}>
      <div className={styles.modal} onClick={(e) => e.stopPropagation()}>
        <button className={styles.close} onClick={onClose}>
          ×
        </button>

        <section>
          <h3>Назначение приложения</h3>

          <p>
            Приложение предназначено для анализа изображений произведений
            искусства с использованием метода рекурсивного разбиения изображения
            на блоки.
          </p>

          <p>
            В качестве исходных данных используются картины из двух галерей:
          </p>

          <ul>
            <li>
              <a href="https://rusmuseum.ru/" target="_blank" rel="noreferrer">
                Русский музей
              </a>
            </li>

            <li>
              <a
                href="https://www.nationalgallery.org.uk/"
                target="_blank"
                rel="noreferrer"
              >
                Лондонская национальная галерея
              </a>
            </li>
          </ul>
        </section>

        <section>
          <h3>Принцип работы</h3>

          <p>
            Изображение разбивается на 4 равные части. Затем каждая часть может
            быть повторно разбита ещё на 4 блока.
          </p>

          <p>Такой подход называется квадродеревом (quadtree decomposition).</p>

          <p>
            Глубина разбиения определяет количество уровней деления изображения.
          </p>

          <div className={styles.formula}>Количество блоков = 4^глубина</div>
        </section>

        <section>
          <h3>Режимы отображения</h3>

          <ul>
            <li>
              <strong>Сетка</strong> — отображение границ блоков.
            </li>

            <li>
              <strong>Цвет</strong> — каждый блок закрашивается средним цветом.
            </li>
          </ul>
        </section>

        <section>
          <h3>Обработка галереи</h3>

          <p>
            Приложение позволяет автоматически обработать все изображения
            выбранной галереи с заданной глубиной разбиения.
          </p>
        </section>

        <section>
          <h3>Метрика изображения</h3>

          <p>
            Для каждой обработанной картины вычисляется числовая метрика,
            характеризующая особенности изображения.
          </p>

          <p>Итоговая метрика зависит от:</p>

          <ul>
            <li>средней яркости изображения</li>

            <li>различия цветов между блоками</li>
          </ul>

          <h4>Количество блоков</h4>

          <p>Количество блоков зависит от глубины разбиения изображения.</p>

          <div className={styles.formula}>N = 4^d</div>

          <ul>
            <li>N — количество блоков</li>
            <li>d — глубина разбиения</li>
          </ul>

          <h4>Относительная яркость изображения</h4>

          <p>
            Для оценки относительной яркости используется взвешенная сумма
            RGB-компонент.
          </p>

          <div className={styles.formula}>L = 0.299R + 0.587G + 0.114B</div>

          <p>(стандарт SDTV/REC.601)</p>

          <ul>
            <li>R — красный канал</li>
            <li>G — зелёный канал</li>
            <li>B — синий канал</li>
            <li>L — относительная яркость изображения</li>
          </ul>

          <p>
            Коэффициенты выбраны с учётом особенностей восприятия цвета
            человеческим глазом.
          </p>

          <h4>Цветовая неоднородность</h4>

          <p>
            После разбиения изображения для каждого блока вычисляется его
            средний цвет.
          </p>

          <p>
            Далее определяется степень отличия каждого блока от среднего цвета
            изображения.
          </p>

          <div className={styles.formula}>
            V = √((Ri - Ravg)^2 + (Gi - Gavg)^2 + (Bi - Bavg)^2)
          </div>

          <ul>
            <li>Ri, Gi, Bi — цвет блока</li>

            <li>Ravg, Gavg, Bavg — средний цвет изображения</li>

            <li>V — мера цветовой неоднородности</li>
          </ul>

          <p>
            Для вычисления V используется евклидово расстояние между цветом
            блока и средним цветом изображения в RGB-пространстве.
          </p>

          <h4>Итоговая метрика</h4>

          <p>Итоговая метрика изображения вычисляется по формуле:</p>

          <div className={styles.formula}>M = kL × L + kV × V</div>

          <ul>
            <li>M — итоговая метрика</li>
            <li>L — относительная яркость изображения</li>
            <li>V — мера цветовой неоднородности</li>
            <li>kL — коэффициент влияния яркости</li>
            <li>kV — коэффициент влияния неоднородности</li>
          </ul>

          <p>
            Изменение коэффициентов позволяет исследовать влияние различных
            характеристик изображения на итоговую сортировку картин.
          </p>

          <p>Чем сильнее различаются блоки, тем выше итоговая метрика.</p>

          <p>
            Это позволяет выполнять сортировку и сравнительный анализ
            изображений.
          </p>
        </section>

        <section>
          <h3>Сортировка обработанных изображений</h3>

          <p>
            Обработанные картины могут быть отсортированы по возрастанию или
            убыванию вычисленной метрики.
          </p>
        </section>
      </div>
    </div>
  );
}
