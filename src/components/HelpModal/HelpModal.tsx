import styles from "./HelpModal.module.css";

type Props = {
  open: boolean;
  onClose: () => void;
};

export default function HelpModal({ open, onClose }: Props) {
  if (!open) return null;

  return (
    <div className={styles.overlay} onClick={onClose}>
      <div className={styles.modal} onClick={(e) => e.stopPropagation()}>
        <button className={styles.close} onClick={onClose}>
          ×
        </button>

        <section>
          <h3>Назначение приложения</h3>

          <p>
            Приложение предназначено для анализа изображений произведений
            искусства с использованием метода рекурсивного разбиения изображения
            на блоки.
          </p>

          <p>
            В качестве исходных данных используются картины из двух галерей:
          </p>

          <ul>
            <li>
              <a href="https://rusmuseum.ru/" target="_blank" rel="noreferrer">
                Русский музей
              </a>
            </li>

            <li>
              <a
                href="https://www.nationalgallery.org.uk/"
                target="_blank"
                rel="noreferrer"
              >
                Лондонская национальная галерея
              </a>
            </li>
          </ul>
        </section>

        <section>
          <h3>Принцип работы</h3>

          <p>
            Изображение разбивается на 4 равные части. Затем каждая часть может
            быть повторно разбита ещё на 4 блока.
          </p>

          <p>Такой подход называется квадродеревом (quadtree decomposition).</p>

          <p>
            Глубина разбиения определяет количество уровней деления изображения.
          </p>

          <div className={styles.formula}>Количество блоков = 4^глубина</div>
        </section>

        <section>
          <h3>Режимы отображения</h3>

          <ul>
            <li>
              <strong>Сетка</strong> — отображение границ блоков.
            </li>

            <li>
              <strong>Цвет</strong> — каждый блок закрашивается средним цветом.
            </li>
          </ul>
        </section>

        <section>
          <h3>Обработка галереи</h3>

          <p>
            Приложение позволяет автоматически обработать все изображения
            выбранной галереи с заданной глубиной разбиения.
          </p>
        </section>

        <section>
          <h3>Метрика изображения</h3>

          <p>
            Для каждой обработанной картины вычисляется числовая метрика,
            характеризующая особенности изображения.
          </p>

          <p>Итоговая метрика зависит от:</p>

          <ul>
            <li>средней яркости изображения</li>

            <li>различия цветов между блоками</li>
          </ul>

          <h4>Количество блоков</h4>

          <p>Количество блоков зависит от глубины разбиения изображения.</p>

          <div className={styles.formula}>N = 4^d</div>

          <ul>
            <li>N — количество блоков</li>
            <li>d — глубина разбиения</li>
          </ul>

          <h4>Относительная яркость изображения</h4>

          <p>
            Для оценки относительной яркости используется взвешенная сумма
            RGB-компонент.
          </p>

          <div className={styles.formula}>L = 0.299R + 0.587G + 0.114B</div>

          <p>(стандарт SDTV/REC.601)</p>

          <ul>
            <li>R — красный канал</li>
            <li>G — зелёный канал</li>
            <li>B — синий канал</li>
            <li>L — относительная яркость изображения</li>
          </ul>

          <p>
            Коэффициенты выбраны с учётом особенностей восприятия цвета
            человеческим глазом.
          </p>

          <h4>Цветовая неоднородность</h4>

          <p>
            После разбиения изображения для каждого блока вычисляется его
            средний цвет.
          </p>

          <p>
            Далее определяется степень отличия каждого блока от среднего цвета
            изображения.
          </p>

          <div className={styles.formula}>
            V = √((Ri - Ravg)^2 + (Gi - Gavg)^2 + (Bi - Bavg)^2)
          </div>

          <ul>
            <li>Ri, Gi, Bi — цвет блока</li>

            <li>Ravg, Gavg, Bavg — средний цвет изображения</li>

            <li>V — мера цветовой неоднородности</li>
          </ul>

          <p>
            Для вычисления V используется евклидово расстояние между цветом
            блока и средним цветом изображения в RGB-пространстве.
          </p>

          <h4>Итоговая метрика</h4>

          <p>Итоговая метрика изображения вычисляется по формуле:</p>

          <div className={styles.formula}>M = kL × L + kV × V</div>

          <ul>
            <li>M — итоговая метрика</li>
            <li>L — относительная яркость изображения</li>
            <li>V — мера цветовой неоднородности</li>
            <li>kL — коэффициент влияния яркости</li>
            <li>kV — коэффициент влияния неоднородности</li>
          </ul>

          <p>
            Изменение коэффициентов позволяет исследовать влияние различных
            характеристик изображения на итоговую сортировку картин.
          </p>

          <p>Чем сильнее различаются блоки, тем выше итоговая метрика.</p>

          <p>
            Это позволяет выполнять сортировку и сравнительный анализ
            изображений.
          </p>
        </section>

        <section>
          <h3>Сортировка обработанных изображений</h3>

          <p>
            Обработанные картины могут быть отсортированы по возрастанию или
            убыванию вычисленной метрики.
          </p>
        </section>

        <section>
          <h3>Классификация изображений</h3>

          <p>
            Для сравнения изображений используется метод классификации на основе
            бинарного пространства признаков и косинусной меры сходства.
          </p>

          <h4>Пространство признаков</h4>

          <p>
            Для каждой картины автоматически формируется набор бинарных
            признаков:
          </p>

          <div className={styles.formula}>(Т Д Тп З М)</div>

          <ul>
            <li>
              <strong>Т</strong> — тёмная картина:
              <br />
              1, если средняя яркость изображения L &lt; 110, иначе 0
            </li>

            <li>
              <strong>Д</strong> — высокая детализация:
              <br />
              1, если мера цветовой неоднородности V &gt; 70, иначе 0
            </li>

            <li>
              <strong>Тп</strong> — тёплая цветовая палитра:
              <br />
              1, если среднее значение красного канала Ravg &gt; Bavg, иначе 0
            </li>

            <li>
              <strong>З</strong> — преобладание зелёного цвета:
              <br />
              1, если Gavg &gt; Ravg и Gavg &gt; Bavg, иначе 0
            </li>

            <li>
              <strong>М</strong> — монохромность изображения:
              <br />
              1, если разница между RGB-компонентами менее 15:
              <br />
              |R-G| &lt; 15, |R-B| &lt; 15, |G-B| &lt; 15
            </li>
          </ul>

          <h4>Бинарный вектор изображения</h4>

          <p>
            После анализа изображения формируется бинарный вектор признаков.
          </p>

          <div className={styles.formula}>(1 0 1 0 0)</div>

          <p>Данный пример означает:</p>

          <ul>
            <li>изображение является тёмным</li>
            <li>детализация низкая</li>
            <li>используется тёплая палитра</li>
            <li>зелёный цвет не доминирует</li>
            <li>изображение не является монохромным</li>
          </ul>

          <h4>Косинусная мера сходства</h4>

          <p>
            Для определения степени сходства между изображениями используется
            косинусная мера.
          </p>

          <p>Сравнение выполняется между бинарными векторами признаков.</p>

          <div className={styles.formula}>cos(A,B) = (A · B) / (|A| × |B|)</div>

          <p>
            Для бинарного пространства признаков формула может быть записана в
            развёрнутом виде:
          </p>

          <div className={styles.formula}>
            cos(A,B) = (a₁b₁ + a₂b₂ + a₃b₃ + a₄b₄ + a₅b₅) / ( √(a₁² + a₂² + a₃²
            + a₄² + a₅²) × (b₁² + b₂² + b₃² + b₄² + b₅²) )
          </div>

          <ul>
            <li>A и B — векторы признаков изображений</li>
            <li>A · B — скалярное произведение</li>
            <li>|A| и |B| — длины векторов</li>
          </ul>

          <h4>Сравнение класса изображений</h4>

          <p>
            После формирования первого класса сравнение продолжается уже не
            между двумя изображениями, а между группой изображений и новым
            кандидатом.
          </p>

          <p>
            В этом случае используется обобщённая косинусная мера для нескольких
            векторов признаков.
          </p>

          <p>Для трёх изображений формула принимает вид:</p>

          <div className={styles.formula}>
            cos(A,B,C) = (a₁b₁c₁ + a₂b₂c₂ + a₃b₃c₃ + a₄b₄c₄ + a₅b₅c₅) /
            √((a₁²+a₂²+a₃²+a₄²+a₅²) × (b₁²+b₂²+b₃²+b₄²+b₅²) ×
            (c₁²+c₂²+c₃²+c₄²+c₅²))
          </div>

          <p>
            Таким образом определяется степень сходства нового изображения с уже
            сформированным классом.
          </p>

          <p>
            Далее аналогичным образом сравнение может выполняться для 4, 5, 6 и
            более изображений.
          </p>

          <p>
            На каждом шаге в класс добавляется изображение, имеющее максимальную
            степень сходства с текущей группой.
          </p>

          <p>
            Чем больше совпадающих признаков имеют изображения, тем ближе
            значение косинуса к 1.
          </p>

          <ul>
            <li>1.0 — изображения максимально похожи</li>
            <li>0.0 — общих признаков нет</li>
          </ul>

          <h4>Построение дерева классов</h4>

          <p>Классификация выполняется пошагово.</p>

          <ol>
            <li>Находится наиболее похожая пара изображений</li>

            <li>Формируется первый класс</li>

            <li>
              Далее последовательно добавляются изображения, наиболее похожие на
              уже сформированный класс
            </li>

            <li>
              После каждого шага вычисляется новая степень сходства класса
            </li>

            <li>Результат отображается в виде дерева классов</li>
          </ol>
        </section>
      </div>
    </div>
  );
}
